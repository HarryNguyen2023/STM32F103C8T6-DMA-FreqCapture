// Function to handle input capture callback 
void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef* htim)
{
  // Check if the callback source is from TIMER2
  if(htim->Instance == TIM2)
  {
    // Case we get the first edge
    if(state == IDLE)
    {
      // Restart the DMA but to the another register
      HAL_TIM_IC_Start_DMA(htim, TIM_CHANNEL_1, sencondEdge, 1);
      // Set the number of total ticks to 0
      ticksCount = 0;
      // Chnange the current state
      state = DONE;
    }
    // Case we get the second edge of the pulse
    else if(state == DONE)
    {
      // Calculate the input frequency 
      resFreq = sencondEdge + (ticksCount * PRELOAD) - firstEdge;
      resFreq = (uint32_t)SYS_FREQ / resFreq;

      // Transmit the value to the computer via UART
      sprintf(send_msg, "Frequency: %lu Hz", resFreq);
      HAL_UART_Transmit(&huart1, send_msg, sizeof(send_msg), 100);
      
      // Restart the DMA but to the another register
      HAL_TIM_IC_Start_DMA(htim, TIM_CHANNEL_1, firstEdge, 1);

      // Update the new state
      state = IDLE;
    }
  }
}

